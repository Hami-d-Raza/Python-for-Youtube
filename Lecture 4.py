# Exercise = Kilometers to Miles Convertor

# Create a program that asks the user to enter a distance in kilometers, and then converts that distance to miles.and then display its ouput to user.

# 1 Miles = 1.609344 Kilometers



# solution
km = float(input("Enter the distance in kilometers:"))
miles = km/ 1.609344

print("The distance in miles is : ", round(miles,5))